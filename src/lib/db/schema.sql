-- Automatically create public.users row on signup
create or replace function public.handle_new_user()
returns trigger 
language plpgsql 
security definer
set search_path = public
as $$
declare
  new_household_id uuid;
begin
  -- Create a new household for the user
  insert into public.households (name) 
  values (split_part(new.email, '@', 1) || '''s Household')
  returning id into new_household_id;

  -- Create user linked to household
  insert into public.users (id, display_name, metabolic_state_json, streak_count, household_id)
  values (
    new.id,
    split_part(new.email, '@', 1),
    '{"current_goal":"maintenance"}',
    0,
    new_household_id
  );
  return new;
end;
$$;

-- Re-create trigger
drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();

