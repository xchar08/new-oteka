-- Enable Extensions
create extension if not exists vector;
create extension if not exists pg_cron;

-- 1. HOUSEHOLDS
create table if not exists households (
  id uuid default gen_random_uuid() primary key,
  name text not null,
  created_at timestamptz default now()
);

-- 2. USERS
create table if not exists users (
  id uuid references auth.users not null primary key,
  display_name text,
  metabolic_state_json jsonb default '{"current_goal": "maintenance"}'::jsonb,
  hand_width_mm numeric,
  streak_count integer default 0,
  household_id uuid references households(id),
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

-- 3. FOODS
create table if not exists foods (
  id bigint generated by default as identity primary key,
  name text not null,
  density_coefficient numeric default 1.0, -- g/cm^3
  category_decay_rate numeric default 0.05, -- daily probability decay
  barcode_upc text unique,
  nutritional_info jsonb default '{}'::jsonb,
  created_at timestamptz default now()
);

-- 4. PANTRY
create table if not exists pantry (
  id bigint generated by default as identity primary key,
  user_id uuid references users(id) on delete cascade not null,
  household_id uuid references households(id),
  food_id bigint references foods(id),
  name text, -- fallback if food_id is null
  probability_score numeric default 1.0,
  last_verified_at timestamptz default now(),
  status text default 'active', -- active, review_needed, consumed
  created_at timestamptz default now()
);

-- 5. FRIENDSHIPS
create table if not exists friendships (
  id bigint generated by default as identity primary key,
  user_id uuid references users(id) not null,
  friend_id uuid references users(id) not null,
  status text default 'pending', -- pending, accepted, blocked
  created_at timestamptz default now(),
  unique(user_id, friend_id)
);

-- 6. CONDITIONS
create table if not exists conditions (
  id text primary key, -- e.g., 'type-1-diabetes', 'keto-strict'
  name text not null,
  category text,
  strictness text,
  diet_impact text,
  rules_json jsonb not null default '{}'::jsonb,
  dga_score numeric default 1.0,
  never_recommend_json jsonb default '[]'::jsonb,
  created_at timestamptz default now()
);

-- 7. USER_CONDITIONS
create table if not exists user_conditions (
  id bigint generated by default as identity primary key,
  user_id uuid references users(id) not null,
  condition_id text references conditions(id) not null,
  created_at timestamptz default now(),
  unique(user_id, condition_id)
);

-- 8. METABOLIC PHENOMENA
create table if not exists metabolic_phenomena (
  id text primary key,
  name text not null,
  category text,
  mechanism text,
  deepseek_prompt_template text not null,
  created_at timestamptz default now()
);

-- 9. WORKFLOWS
create table if not exists workflows (
  id uuid default gen_random_uuid() primary key,
  user_id uuid references users(id) on delete cascade not null,
  trigger_event text not null,
  last_run_status text default 'pending',
  logs_json jsonb default '[]'::jsonb,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

-- 10. LOGS
create table if not exists logs (
  id bigint generated by default as identity primary key,
  user_id uuid references users(id) on delete cascade not null,
  workflow_id uuid references workflows(id),
  grams numeric,
  metabolic_tags_json jsonb default '[]'::jsonb,
  captured_at timestamptz default now()
);

-- 11. CACHE ENTRIES
create table if not exists cache_entries (
  key text primary key,
  value jsonb not null,
  expires_at timestamptz not null
);
create index if not exists idx_cache_expires on cache_entries(expires_at);

-- ========================================================
-- ENTROPY LOGIC (Daily Probability Decay)
-- ========================================================

-- Function to run the decay cycle
create or replace function run_entropy_cycle()
returns void
language plpgsql
security definer
as $$
begin
  -- Update probability scores
  -- Formula: P_new = P_old * (1 - k)
  update pantry
  set 
    probability_score = greatest(0, probability_score * (1 - coalesce(foods.category_decay_rate, 0.05))),
    status = case 
      when (probability_score * (1 - coalesce(foods.category_decay_rate, 0.05))) < 0.3 then 'review_needed'
      else status
    end
  from foods
  where pantry.food_id = foods.id
  and pantry.status = 'active';
end;
$$;

-- Schedule the job (Runs daily at 00:00 GMT)
-- Requires pg_cron to be active in Supabase
select cron.schedule(
  'entropy-daily-cycle',
  '0 0 * * *', 
  'select run_entropy_cycle()'
);

